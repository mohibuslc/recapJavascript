


// Make find Out Wich Number are bigger in the Array ; 



var value = [ 12 , 28 , 44, 56, 190, 330 ,1009];

var max = value[0];

for(var i =0 ; i<value.length ; i++){

    var element = value[i]; 

    if( element > max ){

        max = element ; 

    }
}

console.log(" Here Bigger Value is = " , max );



